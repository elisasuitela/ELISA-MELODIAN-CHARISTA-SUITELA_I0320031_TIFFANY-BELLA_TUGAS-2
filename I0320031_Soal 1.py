# Luas Persegi panjang
print("Menghitung Luas Persegi Panjang")

# masukkan Nilai Panjang
Panjang = float(input("Masukkan Nilai Panjang Persegi Panjang: "))
# masukkan Nilai Lebar
Lebar = float(input("Masukkan Nilai Lebar Persegi Panjang: "))

# menghitung Luas Persegi Panjang
Luas_Persegi_Panjang = Panjang * Lebar

# menampilkan hasil luas persegi panjnag
print("Luas Persegi Panjang: ", Luas_Persegi_Panjang)


# Luas Lingkaran
print("Menghitung Luas Lingkaran")

# Masukkan Nilai Jari - Jari
r = float(input("Masukkan Nilai Jari - Jari Lingkaran: "))

# Menghitung Luas Lingkaran
luas = 3.14 * (r ** 2)

# menampilkan hasil luas lungkaran
print("Luas Lingkaran: ", luas)


# Luas Kubus
print("Menghitung Luas Kubus")

# masaukkan nilai sisi kubus
sisi = float(input("Sisi Kubus: "))

# Menhitung Luas Kubus
luas = 6 * (sisi ** 6)

# Menampilkan Hasil
print("Luas Kubus: ", luas)


# Konversi Suhu Celcius ke Farenheit
print("Mengkonversi Suhu Celsius ke Fahrenheit")

# Masukkan Nilai Suhu Dalam Celcius
celcius = float(input("Masukkan Suhu Dalam Celcius: "))

# hitung konversi
farenheit = ((9 / 5) * celcius) + 32

# tampilkan Hasil
print("hasil konversi: ", farenheit)


# Menghitung Konversi Reamur ke Kelvin
print("mengkonversi Suhu Reamur Ke Kelvin")

# Memasukkan Nilai Suhu dalam Reamur
reamur = float(input("Masukkan Nilai Suhu Dalam Reamur"))

# mengkonversi Reamur ke Kelvin
kelvin = ((5 / 4) * reamur) + 273

#menampilkan hasil
print("Hasil Konversi Suhu Reamur ke Kelvin: ", kelvin)
